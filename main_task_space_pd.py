import time
from pathlib import Path

import numpy as np
import mujoco
import mujoco.viewer

from kinematics import forward_kinematics, jacobian


# ============================================================
# 1. MuJoCo 模型路径
# ============================================================
MODEL_PATH = Path(__file__).parent / "models" / "two_link_arm.xml"


# ============================================================
# 2. 末端空间 PD 力矩控制器
# ============================================================
def task_space_pd_control(q, dq, x_des, dx_des):
    """
    末端空间 PD 力矩控制器。

    输入：
        q      : 当前关节角度，形状为 [2]
        dq     : 当前关节角速度，形状为 [2]
        x_des  : 目标末端位置，形状为 [2]，例如 [x_des, y_des]
        dx_des : 目标末端速度，形状为 [2]

    输出：
        tau    : 两个关节需要施加的力矩，形状为 [2]

    控制思路：
        1. 根据 q 算出当前末端位置 x
        2. 根据 q 算出雅可比矩阵 J
        3. 根据 J 和 dq 算出当前末端速度 dx
        4. 在末端空间做 PD 控制，得到末端虚拟力 F
        5. 用 tau = J.T @ F 把末端力转换成关节力矩
    """

    # ------------------------------------------------------------
    # 2.1 计算当前末端位置
    # ------------------------------------------------------------
    # x 是二维向量：
    # x[0] 表示末端 x 坐标
    # x[1] 表示末端 y 坐标
    x = forward_kinematics(q)

    # ------------------------------------------------------------
    # 2.2 计算雅可比矩阵
    # ------------------------------------------------------------
    # J 的作用：
    # 把关节速度 dq 转换为末端速度 dx
    #
    # dx = J @ dq
    J = jacobian(q)

    # ------------------------------------------------------------
    # 2.3 计算当前末端速度
    # ------------------------------------------------------------
    # dq 是关节速度
    # dx 是末端速度
    dx = J @ dq

    # ------------------------------------------------------------
    # 2.4 设置末端空间 PD 参数
    # ------------------------------------------------------------
    # Kp_task 越大，末端越想快速靠近目标点
    Kp_task = np.array([60.0, 60.0])

    # Kd_task 越大，末端运动越不容易振荡
    Kd_task = np.array([10.0, 10.0])

    # ------------------------------------------------------------
    # 2.5 计算末端位置误差和速度误差
    # ------------------------------------------------------------
    position_error = x_des - x
    velocity_error = dx_des - dx

    # ------------------------------------------------------------
    # 2.6 计算末端虚拟力
    # ------------------------------------------------------------
    # F 不是 MuJoCo 直接施加的外力；
    # 它是我们在控制器里构造出来的“虚拟力”。
    #
    # F[0] 表示希望末端在 x 方向受到的力
    # F[1] 表示希望末端在 y 方向受到的力
    F = Kp_task * position_error + Kd_task * velocity_error

    # ------------------------------------------------------------
    # 2.7 把末端力转换成关节力矩
    # ------------------------------------------------------------
    # tau = J.T @ F 是机器人控制里非常重要的关系。
    #
    # 直观理解：
    # 末端需要一个力 F；
    # 但电机只能给关节施加力矩；
    # 所以用 J.T 把末端力映射回关节力矩。
    tau = J.T @ F

    return tau, x, dx, F, J


# ============================================================
# 3. 主程序
# ============================================================
def main():
    # ------------------------------------------------------------
    # 3.1 读取 MuJoCo 模型
    # ------------------------------------------------------------
    model = mujoco.MjModel.from_xml_path(str(MODEL_PATH))
    data = mujoco.MjData(model)

    # ------------------------------------------------------------
    # 3.2 设置初始关节状态
    # ------------------------------------------------------------
    # 这里不从 [0, 0] 开始，是因为 [0, 0] 是完全伸直姿态，
    # 对某些方向的末端控制不太友好。
    #
    # 所以我们给它一个稍微弯曲的初始姿态。
    data.qpos[:] = np.array([0.3, -0.9])
    data.qvel[:] = np.array([0.0, 0.0])

    # ------------------------------------------------------------
    # 3.3 设置目标末端位置
    # ------------------------------------------------------------
    # 这次我们不再设置 q_des。
    # 我们直接设置末端红点想去的位置。
    #
    # target_site 是绿色目标点。

    target_site_id = model.site("target_site").id
    mujoco.mj_forward(model, data)
    # 读取绿色目标点在 MuJoCo 世界坐标系中的位置
    target_pos_3d = data.site_xpos[target_site_id].copy()

    # 控制器只控制 xy 平面，所以取前两个分量
    x_des = target_pos_3d[:2]

    # 目标末端速度
    # 希望最后停在目标点，所以目标速度为 0。
    dx_des = np.array([0.0, 0.0])

    # ------------------------------------------------------------
    # 3.4 更新 MuJoCo 内部状态
    # ------------------------------------------------------------
    mujoco.mj_forward(model, data)

    # 获取末端 site 的 id
    ee_site_id = model.site("ee_site").id

    # 打印基本信息
    print("Model loaded successfully.")
    print("Model path:", MODEL_PATH)
    print("Number of joints:", model.njnt)
    print("Number of actuators:", model.nu)
    print("Target end-effector position x_des:", x_des)

    # 计数器，用来控制打印频率
    step_count = 0

    # ------------------------------------------------------------
    # 3.5 打开 MuJoCo viewer
    # ------------------------------------------------------------
    with mujoco.viewer.launch_passive(model, data) as viewer:

        while viewer.is_running():
            step_start = time.time()

            # ----------------------------------------------------
            # 读取当前关节状态
            # ----------------------------------------------------
            q = data.qpos.copy()
            dq = data.qvel.copy()

            # ----------------------------------------------------
            # 计算末端空间 PD 控制力矩
            # ----------------------------------------------------
            tau, x, dx, F, J = task_space_pd_control(
                q=q,
                dq=dq,
                x_des=x_des,
                dx_des=dx_des
            )

            # ----------------------------------------------------
            # 限制关节力矩
            # ----------------------------------------------------
            tau = np.clip(tau, -20.0, 20.0)

            # ----------------------------------------------------
            # 把关节力矩输入给 MuJoCo
            # ----------------------------------------------------
            data.ctrl[:] = tau

            # ----------------------------------------------------
            # 推进仿真
            # ----------------------------------------------------
            mujoco.mj_step(model, data)

            # 更新 viewer
            viewer.sync()

            # ----------------------------------------------------
            # 每隔 500 步打印一次信息
            # ----------------------------------------------------
            step_count += 1

            if step_count % 500 == 0:
                ee_pos_mujoco = data.site_xpos[ee_site_id].copy()

                print("\n================ Task Space PD Check ================")
                print("Current q:", q)
                print("Current dq:", dq)

                print("\nTarget end-effector position x_des:")
                print(x_des)

                print("\nEnd-effector position from kinematics x:")
                print(x)

                print("\nEnd-effector position from MuJoCo:")
                print(ee_pos_mujoco[:2])

                print("\nPosition error x_des - x:")
                print(x_des - x)

                print("\nEnd-effector velocity dx:")
                print(dx)

                print("\nVirtual task-space force F:")
                print(F)

                print("\nJoint torque tau:")
                print(tau)

                print("\nJacobian J:")
                print(J)
                print("=====================================================")

            # ----------------------------------------------------
            # 控制仿真速度接近真实时间
            # ----------------------------------------------------
            dt = model.opt.timestep
            elapsed = time.time() - step_start

            if elapsed < dt:
                time.sleep(dt - elapsed)


# ============================================================
# 4. 程序入口
# ============================================================
if __name__ == "__main__":
    main()