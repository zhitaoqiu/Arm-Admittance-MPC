import numpy as np


# ============================================================
# 二自由度机械臂的几何参数
# ============================================================
# 这里的长度要和 XML 文件里保持一致：
#
# link1:
# <geom name="link1_geom" fromto="0 0 0 0.5 0 0"/>
#
# link2:
# <geom name="link2_geom" fromto="0 0 0 0.4 0 0"/>
#
# 所以：
# L1 = 0.5
# L2 = 0.4
L1 = 0.5
L2 = 0.4


def forward_kinematics(q):
    """
    二自由度平面机械臂正运动学。

    输入：
        q: 两个关节角度
           q[0] = q1
           q[1] = q2

    输出：
        ee_pos: 末端位置 [x, y]

    作用：
        根据关节角度，计算末端执行器在平面中的位置。
    """

    # 取出两个关节角
    q1 = q[0]
    q2 = q[1]

    # ------------------------------------------------------------
    # 末端 x 坐标
    # ------------------------------------------------------------
    # 第一根杆贡献：
    # L1 * cos(q1)
    #
    # 第二根杆贡献：
    # L2 * cos(q1 + q2)
    #
    # 为什么是 q1 + q2？
    # 因为第二根杆的角度是相对于第一根杆转的，
    # 所以它在世界坐标系里的总角度 = q1 + q2
    x = L1 * np.cos(q1) + L2 * np.cos(q1 + q2)

    # ------------------------------------------------------------
    # 末端 y 坐标
    # ------------------------------------------------------------
    # 第一根杆贡献：
    # L1 * sin(q1)
    #
    # 第二根杆贡献：
    # L2 * sin(q1 + q2)
    y = L1 * np.sin(q1) + L2 * np.sin(q1 + q2)

    # 返回一个 numpy 数组，形式是 [x, y]
    ee_pos = np.array([x, y])

    return ee_pos


def jacobian(q):
    """
    二自由度平面机械臂雅可比矩阵。

    这一步先写出来备用。
    下一步做末端速度、阻抗控制时会用到它。

    输入：
        q: 两个关节角度

    输出：
        J: 2x2 雅可比矩阵

    作用：
        把关节速度 dq 转换成末端速度 dx。

        dx = J(q) @ dq
    """

    q1 = q[0]
    q2 = q[1]

    # x 对 q1 的偏导
    j11 = -L1 * np.sin(q1) - L2 * np.sin(q1 + q2)

    # x 对 q2 的偏导
    j12 = -L2 * np.sin(q1 + q2)

    # y 对 q1 的偏导
    j21 = L1 * np.cos(q1) + L2 * np.cos(q1 + q2)

    # y 对 q2 的偏导
    j22 = L2 * np.cos(q1 + q2)

    J = np.array([
        [j11, j12],
        [j21, j22],
    ])

    return J